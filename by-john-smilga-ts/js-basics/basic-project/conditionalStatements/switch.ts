// switch
// dice value 1-6

const dice = 1;

switch (dice) {
    case 1:
        console.log("You got One");
        break;
    case 2:
        console.log("You got Two");
        break;
    case 3:
        console.log("You got Three");
        break;
    default :
        console.log("you didnt roll dice");    
};